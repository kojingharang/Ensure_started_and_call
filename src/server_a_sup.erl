%% @doc
-module(server_a_sup).

-behaviour(supervisor).

%%----------------------------------------------------------------------------------------------------------------------
%% Exported API
%%----------------------------------------------------------------------------------------------------------------------
-export([
         start_link/0,
         start_child/1
        ]).

%%----------------------------------------------------------------------------------------------------------------------
%% 'supervisor' Callback API
%%----------------------------------------------------------------------------------------------------------------------
-export([init/1]).

%%----------------------------------------------------------------------------------------------------------------------
%% Exported Functions
%%----------------------------------------------------------------------------------------------------------------------
-spec start_link() -> {ok, pid()} | {error, Reason::term()}.
start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).

-spec start_child(binary()) -> {ok, pid()} | {error, Reason::term()}.
start_child(Id) ->
    supervisor:start_child(?MODULE, [Id]).

%%----------------------------------------------------------------------------------------------------------------------
%% 'supervisor' Callback API
%%----------------------------------------------------------------------------------------------------------------------
%% @private
init([]) ->
    ServerA = server_a,
    Spec =
        [
         {ServerA, {ServerA, start_link, []}, temporary, 5000, worker, [ServerA]}
        ],
    {ok, { {simple_one_for_one, 5, 10}, Spec} }.
